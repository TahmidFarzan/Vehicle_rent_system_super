     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(FeedbackStoreRequest $request)
    {
        $pf = new Feedback();
           $pf->name=$request['name'];
           $pf->message=$request['message'];
           $pf->email=$request['email'];
           if($pf->save()){
              return redirect()->route('contact-us.index')->with(['success'=>'Feedback send.']);
             }
             else{
              return redirect()->route('contact-us.index')->with(['error'=>'Unable to send feedback.']);
             }  
    }

